        <h3>Вместо предисловия</h3>

        <p><b>Из беседы критика и литературоведа Тересы Валяс с Виславой Шимборской</b></p>

        <p><b>Тереса Валяс.</b> Кто в&nbsp;&laquo;<span class="tonote" id="tonote001">Жицелитерацке</span>&raquo;
            придумал&nbsp;&laquo;Почту&raquo;?</p>
        <p><b>Вислава Шимборская.</b> Придумывать ничего не&nbsp;понадобилось. Это давняя традиция, практиковавшаяся в&nbsp;литературных
            журналах. Как отвечать автору, в&nbsp;особенности начинающему? Не&nbsp;писать&nbsp;же всем подробные письма.
            Обычно ограничиваются кратким &laquo;не&nbsp;можем опубликовать&raquo; или &laquo;советуем ещё поработать&raquo;.
            Мы&nbsp;сочли, что иногда следует наши решения обосновывать.&nbsp;&lt;&hellip;&gt;</p>
        <p><b>Т.&thinsp;В.</b> Отвергая то&nbsp;или иное произведение и, вероятно, представляя себе беззащитного,
            дрожащего автора, ты&nbsp;никогда не&nbsp;чувствовала себя&nbsp;бессердечной?</p>
        <p><b>В.&thinsp;Ш.</b> Бессердечной? Да&nbsp;я&nbsp;сама начинала с&nbsp;плохих стихов и&nbsp;плохих рассказов.
            И&nbsp;знаю, что ушат холодной воды на&nbsp;голову&nbsp;&mdash; отличное терапевтическое средство. А&nbsp;вот
            когда особа, именующая себя педагогом, пишет &laquo;сровнение&raquo;, я&nbsp;становлюсь&nbsp;безжалостной.
        </p>
        <p><b>Т.&thinsp;В.</b> Ну, это просто безграмотность и&nbsp;к&nbsp;искусству отношения&nbsp;не&nbsp;имеет.</p>
        <p><b>В.&thinsp;Ш.</b> На&nbsp;уровне &laquo;Почты&raquo; об&nbsp;искусстве речи не&nbsp;шло. Я&nbsp;старалась
            объяснять элементарные вещи, советовала хорошенько обдумывать то, что пишешь, относиться к&nbsp;себе хоть
            мало&mdash;мальски критически. Наконец, призывала читать книги. Возможно, я&nbsp;ошибаюсь, но, хочется
            надеяться, кое у&nbsp;кого эта прекрасная привычка останется на&nbsp;всю жизнь.&nbsp;&lt;&hellip;&gt;</p>
        <p><b>Т.&thinsp;В.</b> Ты&nbsp;всегда была уверена в&nbsp;правильности своих&nbsp;оценок?</p>
        <p><b>В.&thinsp;Ш.</b> Не&nbsp;всегда&nbsp;&mdash; только в&nbsp;случаях ярко выраженной&nbsp;графомании.</p>
        <p><b>Т.&thinsp;В.</b> Графомания&nbsp;&mdash; жестокое слово. Не&nbsp;знаю, приходило&nbsp;ли тебе в&nbsp;голову,
            что в&nbsp;других сферах человеческой деятельности нет столь негативных оценочных понятий. &laquo;Халтурщик&raquo;,
            например, тоже звучит обидно, но&nbsp;ему далеко до&nbsp;&laquo;графомана&raquo;. Плохой столяр, плохой
            сантехник, часовщик-недоучка живут себе преспокойно, и&nbsp;никто не&nbsp;ставит на&nbsp;них клеймо.
            Нападкам подвергаются главным образом писатели-неудачники. И&nbsp;ещё, пожалуй, незадачливые любовники:
            &laquo;импотент&raquo; не&nbsp;менее оскорбителен, чем&nbsp;&laquo;графоман&raquo;.</p>
        <p><b>В.&thinsp;Ш.</b> Да, но&nbsp;графоман&nbsp;&mdash; в&nbsp;своей области&nbsp;&mdash; может! Много может,
            слишком много! Впрочем, если не&nbsp;ошибаюсь, в&nbsp;&laquo;Почте&raquo; я&nbsp;никого не&nbsp;называла
            графоманом. Просто старалась направить повышенную писательскую активность в&nbsp;другое русло. Например,
            рекомендовала писать письма, вести дневник или сочинять для близких стишки на&nbsp;случай.&nbsp;&lt;&hellip;&gt;
        </p>
        <p><b>Т.&thinsp;В.</b> Читая &laquo;Почту&raquo;, я&nbsp;подумала: мало кто находит в&nbsp;себе мужество сказать
            начинающему писателю, что нужно обладать талантом,&nbsp;&mdash; ты&nbsp;одна из&nbsp;немногих. Серьезные
            критики сейчас неохотно употребляют это&nbsp;слово.</p>
        <p><b>В.&thinsp;Ш.</b> И, наверно, правильно делают: талант&nbsp;&mdash; понятие, не&nbsp;поддающееся научному
            определению. Хотя это не&nbsp;означает, что то, чему нельзя дать точное определение, не&nbsp;существует. Но&nbsp;я&nbsp;не&nbsp;критик
            и&nbsp;могу позволить себе некоторую свободу высказываний. Талант&hellip; У&nbsp;одного он&nbsp;есть, у&nbsp;другого
            нет, и&nbsp;все тут. Из&nbsp;чего, впрочем, не&nbsp;следует, что последний&nbsp;&mdash; неудачник. Возможно,
            он&nbsp;станет выдающимся биохимиком или откроет Северный&nbsp;полюс.</p>
        <p><b>Т.&thinsp;В.</b> Насколько я&nbsp;помню, Северный полюс давно&nbsp;открыт.</p>
        <p><b>В.&thinsp;Ш.</b> Верно, это я&nbsp;маху дала. Хотела сказать, что таланты встречаются не&nbsp;только в&nbsp;литературе,
            но&nbsp;и&nbsp;в&nbsp;любых других областях.&nbsp;&lt;&hellip;&gt;</p>
        <p><b>Т.&thinsp;В.</b> Скажи, а&nbsp;с&nbsp;каким чувством ты&nbsp;сейчас перечитывала&nbsp;&laquo;Почту&raquo;?
        </p>
        <p><b>В.&thinsp;Ш.</b> Мне она показалась скорее забавной, чем полезной с&nbsp;дидактической точки зрения. И&nbsp;ответственность
            за&nbsp;это лежит в&nbsp;значительной степени&nbsp;на&nbsp;мне.</p>
